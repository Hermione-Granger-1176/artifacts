#!/usr/bin/env python3
"""Build the shared public stylesheet from ordered source partials.

This module backs ``make styles``. Source styles remain split by ownership in
``css/src`` while browsers load the single generated ``css/style.css`` asset.
"""

from __future__ import annotations

import re
import sys
from typing import TYPE_CHECKING

from scripts import REPO_ROOT

if TYPE_CHECKING:
    from pathlib import Path

SOURCE_DIR = REPO_ROOT / "css" / "src"
OUTPUT_FILE = REPO_ROOT / "css" / "style.css"
SOURCE_FILENAME_PATTERN = re.compile(r"\d{2}-[a-z0-9]+(?:-[a-z0-9]+)*\.css")


def source_files() -> tuple[Path, ...]:
    """Return the ordered shared stylesheet source files."""
    candidates = tuple(sorted(SOURCE_DIR.glob("*.css")))
    invalid_names = [
        source_file.name
        for source_file in candidates
        if SOURCE_FILENAME_PATTERN.fullmatch(source_file.name) is None
    ]
    if invalid_names:
        names = ", ".join(invalid_names)
        raise ValueError(
            f"Invalid stylesheet source filename(s): {names}. "
            "Expected two-digit, kebab-case names such as 01-tokens.css."
        )
    if not candidates:
        raise ValueError(f"No stylesheet source partials found in {SOURCE_DIR}")
    return candidates


def output_header(sources: tuple[Path, ...] | None = None) -> str:
    """Return the generated stylesheet header for the current source list."""
    resolved_sources = sources if sources is not None else source_files()
    if not resolved_sources:
        raise ValueError("At least one stylesheet source is required to build the output header")
    return (
        "/*\n"
        " * Generated shared stylesheet. Do not edit this file directly.\n"
        f" * Source: css/src/{resolved_sources[0].name} "
        f"through css/src/{resolved_sources[-1].name}\n"
        " * Rebuild with: make styles\n"
        " */\n\n"
    )


def build_stylesheet() -> str:
    """Return the deterministic public stylesheet content."""
    sources = source_files()
    return (
        output_header(sources)
        + "\n\n".join(source_file.read_text(encoding="utf-8").strip() for source_file in sources)
        + "\n"
    )


def generate() -> None:
    """Write the public stylesheet from the ordered source partials."""
    OUTPUT_FILE.write_text(build_stylesheet(), encoding="utf-8")


def main(argv: list[str] | None = None) -> int:
    """Run the stylesheet generator CLI."""
    del argv
    generate()
    print(f"Generated {OUTPUT_FILE.relative_to(REPO_ROOT).as_posix()}")
    return 0


if __name__ == "__main__":  # pragma: no cover
    sys.exit(main())
